package com.sun.jmx.remote.socket;

import java.io.IOException;
import java.io.InputStream;
import java.io.OutputStream;
import java.net.Socket;
import javax.security.auth.Subject;

/**
 * This interface specifies methods to manipulate the socket and its input/output streams in a socket-based connection.
 */
public interface SocketConnectionIf {
  /**
   * Returns a reference to the underlying socket.
   * @return a {@link Socket} instance.
   */
  Socket getSocket();

  /**
   * Replaces the underlying socket.
   * @param s a {@link Socket} instance.
   * @throws IOException if any I/O error occurs.
   */
  void setSocket(Socket s) throws IOException;

  /**
   * Replaces the current socket's input/output streams by the ones provided.
   * @param is the input stream to replace with.
   * @param os the output stream to replace with.
   * @throws IOException if any I/O error occurs.
   */
  void replaceStreams(InputStream is, OutputStream os) throws IOException;

  /**
   * Sets the subject authenticated through this socket connection.
   * @param subject the authenticated subjet.
   */
  void setSubject(Subject subject);
}
